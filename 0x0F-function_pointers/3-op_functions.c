#include "3-calc.h"
/**
 * op_add - returns sum of a and b
 * @a: parameter to be used
 * @b: parameter to be used
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference between a and b
 * @a: parameter to be used
 * @b: parameter to be used
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product between a and b
 * @a: parameter to be used
 * @b: parameter to be used
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division between a and b
 * @a: parameter to be used
 * @b: parameter to be used
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns reminder of division between a and b
 * @a: parameter to be used
 * @b: parameter to be used
 * Return: reminder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
